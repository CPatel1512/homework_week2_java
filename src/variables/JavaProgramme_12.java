package variables;

public class JavaProgramme_12 {
    public static void main(String[] args) {
        // Declare the variables for the computation
        double p = 25.5;
        double q = 3.5;
        double r = 40.5;
        double s = 4.5;

        // Compute the specified expression
        double result = ((p * q - q * q) / (r - s));

        // Print the output
        System.out.println("Expected output of the entered data((25.5 * 3.5 - 3.5 * 3.5) / (40.5 - 4.5)) is = " + result);
    }
}
